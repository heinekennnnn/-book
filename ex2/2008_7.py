FirstInput = float(input("input first number: "))
Operator = input("input your operator: ")
SecondInput = float(input("input second number: "))

if(Operator == '+'):
    print(FirstInput + SecondInput)
elif(Operator == '-'):
    print(FirstInput - SecondInput)
elif(Operator == '*'):
    print(FirstInput * SecondInput)
elif(Operator == '%'):
    print(FirstInput * SecondInput)
else:
    print("ERROR NAKRUBB")


